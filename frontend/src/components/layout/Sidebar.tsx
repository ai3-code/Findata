'use client';

import { useState } from 'react';
import Link from 'next/link';
import Image from 'next/image';
import { usePathname } from 'next/navigation';
import { cn } from '@/lib/utils';
import { useAuth } from '@/contexts/AuthContext';

const navigation = [
  { name: 'Dashboard', href: '/', icon: HomeIcon },
  { name: 'Upload Data', href: '/upload', icon: UploadIcon },
  { name: 'Analytics Explorer', href: '/analytics/explorer', icon: ChartBarIcon },
  { name: 'Payment Recovery', href: '/analytics/recovery', icon: CurrencyIcon },
  { name: 'Data Anomalies', href: '/anomalies', icon: AlertIcon },
];

export default function Sidebar() {
  const pathname = usePathname();
  const { user, logout } = useAuth();
  const [imageHidden, setImageHidden] = useState(false);

  return (
    <div className="flex h-full w-64 flex-col bg-gray-900">
      {/* Logo */}
      <div className="flex h-16 items-center px-6">
        <span className="text-xl font-bold text-white">Surgery Billing</span>
      </div>

      {/* Navigation */}
      <nav className="flex-1 space-y-1 px-3 py-4">
        {navigation.map((item) => {
          const isActive = pathname === item.href ||
            (item.href !== '/' && pathname.startsWith(item.href));

          return (
            <Link
              key={item.name}
              href={item.href}
              className={cn(
                'group flex items-center gap-3 rounded-lg px-3 py-2 text-sm font-medium transition-colors',
                isActive
                  ? 'bg-gray-800 text-white'
                  : 'text-gray-300 hover:bg-gray-800 hover:text-white'
              )}
            >
              <item.icon
                className={cn(
                  'h-5 w-5 flex-shrink-0',
                  isActive ? 'text-white' : 'text-gray-400 group-hover:text-white'
                )}
              />
              {item.name}
            </Link>
          );
        })}
      </nav>

      {/* User Info & Logout */}
      <div className="border-t border-gray-800 p-4">
        {/* Special image for adminy user */}
        {user?.username === 'adminy' && !imageHidden && (
          <div className="mb-4 relative">
            <button
              onClick={() => setImageHidden(true)}
              className="absolute top-1 right-1 z-10 h-6 w-6 flex items-center justify-center rounded-full bg-gray-800/80 hover:bg-gray-700 text-gray-300 hover:text-white transition-colors"
              title="Hide image"
            >
              <CloseIcon className="h-4 w-4" />
            </button>
            <Image
              src="/admin-avatar.png"
              alt="Admin"
              width={200}
              height={120}
              className="rounded-lg w-full object-cover"
            />
          </div>
        )}
        {user && (
          <div className="mb-3">
            <div className="flex items-center gap-3">
              <div className="flex h-8 w-8 items-center justify-center rounded-full bg-blue-600 text-sm font-medium text-white">
                {user.firstName?.[0] || user.username?.[0] || 'U'}
              </div>
              <div className="flex-1 min-w-0">
                <p className="text-sm font-medium text-white truncate">
                  {user.fullName || user.username}
                </p>
                <p className="text-xs text-gray-400 truncate">
                  {user.email}
                </p>
              </div>
            </div>
          </div>
        )}
        <button
          onClick={logout}
          className="flex w-full items-center gap-2 rounded-lg px-3 py-2 text-sm font-medium text-gray-300 hover:bg-gray-800 hover:text-white transition-colors"
        >
          <LogoutIcon className="h-5 w-5 text-gray-400" />
          Sign Out
        </button>
        <p className="mt-3 text-xs text-gray-500">Surgery Billing Dashboard v1.0</p>
      </div>
    </div>
  );
}

